const selectedLayerValue = ref(layerOptions[0].value)
const selectedLayer = computed(() => layerOptions.find(opt => opt.value === selectedLayerValue.value)!)

const handleLayerChange = (val: string) => {
  const layer = layerOptions.find(item => item.value === val)
  if (!layer) return ElMessage.warning('图层不存在！')
  setColorConfig(
    {
      factor: layer.value,
      colors: layer.grayColors,
    },
    {
      min: layer.min,
      step: layer.step,
    },
  )
  setColorMapTileLayerOption(layer.url1, layer.value)
  client.renderColorMapTileFunction(map.value, mapInstance.value)
}

onMounted(() => {
  // 初始化地图
  initMap({
    container: 'map',
    center: [108.84, 21.58],
    zoom: 4,
  })

  // 地图加载完成
  map.value?.on('load', () => {
    client.initColorMapTileLayer(map.value!, mapInstance.value!, {
      beforeId: 'darkSourceLayer',
      layerId: 'ColorMapLayer',
    })
    // 初始化颜色和图层
    handleLayerChange(selectedLayerValue.value)
  })
})
