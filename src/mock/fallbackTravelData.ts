import type { RecommendedCity, ScenicSpot, TravelRecommendParams } from '@/types/travelTypes'
import { localFallbackImage } from '@/services/travelConfig'

function spot(
  id: string,
  name: string,
  cityName: string,
  province: string,
  partial: Omit<ScenicSpot, 'id' | 'name' | 'cityName' | 'province' | 'image'> & { image?: string },
): ScenicSpot {
  return {
    id,
    name,
    cityName,
    province,
    image: partial.image || localFallbackImage(),
    imageSource: 'demo-fallback',
    ...partial,
  }
}

